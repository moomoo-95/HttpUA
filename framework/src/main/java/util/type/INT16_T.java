package util.type;

public class INT16_T {

    ////////////////////////////////////////////////////////////
    // VARIABLES
    public short value;
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    public INT16_T() {}

    public INT16_T(short value) {
        this.value = value;
    }
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // FUNCTIONS
    public boolean equal(INT16_T datum) {
        if (datum == null) { return false; }
        return value == datum.value;
    }
    ////////////////////////////////////////////////////////////

}
