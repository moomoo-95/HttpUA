package util.type;

public class INT32_T {

    ////////////////////////////////////////////////////////////
    // VARIABLES
    public int value;
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    public INT32_T() {}

    public INT32_T(int value) {
        this.value = value;
    }
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // FUNCTIONS
    public boolean equal(INT32_T datum) {
        if (datum == null) { return false; }
        return value == datum.value;
    }
    ////////////////////////////////////////////////////////////

}
