package util.type;

public class INT64_T {

    ////////////////////////////////////////////////////////////
    // VARIABLES
    public long value;
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    public INT64_T() {}

    public INT64_T(long value) {
        this.value = value;
    }
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // FUNCTIONS
    public boolean equal(INT64_T datum) {
        if (datum == null) { return false; }
        return value == datum.value;
    }
    ////////////////////////////////////////////////////////////

}
