package util.type;

public class INT8_T {

    ////////////////////////////////////////////////////////////
    // VARIABLES
    public byte value;
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    public INT8_T() {}

    public INT8_T(byte value) {
        this.value = value;
    }
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // FUNCTIONS
    public boolean equal(INT8_T datum) {
        if (datum == null) { return false; }
        return value == datum.value;
    }
    ////////////////////////////////////////////////////////////

}
