package util.type;

import util.parser.UnsignedByte;

public class UINT8_T {

    ////////////////////////////////////////////////////////////
    // VARIABLES
    public short value;
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    public UINT8_T() {}

    public UINT8_T(short value) {
        this.value = value;
    }

    public UINT8_T(byte value) {
        this.value = UnsignedByte.parse(value);
    }
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    // FUNCTIONS
    public boolean equal(UINT8_T datum) {
        if (datum == null) { return false; }
        return value == datum.value;
    }
    ////////////////////////////////////////////////////////////

}
